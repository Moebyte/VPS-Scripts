#!/usr/bin/env bash
set -euo pipefail

BIN_PATH="/usr/local/bin/gost"
CONF_DIR="/etc/gost"
RULES_FILE="${CONF_DIR}/rules.conf"
ENV_FILE="${CONF_DIR}/gost.env"
SERVICE_FILE="/etc/systemd/system/gost.service"
CRON_FILE="/etc/cron.d/gost-restart"
CERT_DIR="${CONF_DIR}/certs"

RED="\033[31m"
GRN="\033[32m"
YEL="\033[33m"
BLU="\033[36m"
RST="\033[0m"

require_root() {
  if [[ "${EUID}" -ne 0 ]]; then
    echo -e "${RED}请用 root 运行此脚本${RST}"
    exit 1
  fi
}

need_cmd() {
  command -v "$1" >/dev/null 2>&1 || {
    echo -e "${YEL}缺少依赖: $1，正在安装...${RST}"
    apt-get update -y >/dev/null 2>&1 || true
    apt-get install -y "$1" >/dev/null 2>&1 || {
      echo -e "${RED}安装依赖失败: $1${RST}"
      exit 1
    }
  }
}

prompt_read() {
  local __var="$1" __prompt="$2" __input=""
  if [[ -r /dev/tty ]]; then
    IFS= read -r -p "${__prompt}" __input < /dev/tty || return 1
  else
    IFS= read -r -p "${__prompt}" __input || return 1
  fi
  printf -v "${__var}" '%s' "${__input}"
}

prepare_env() {
  mkdir -p "${CONF_DIR}" "${CERT_DIR}"
  touch "${RULES_FILE}"
  chmod 600 "${RULES_FILE}"
  [[ -f "${ENV_FILE}" ]] || cat > "${ENV_FILE}" <<'ENV_EOF'
# Auto-generated by gost-manager.sh
GOST_ARGS=
ENV_EOF
  chmod 600 "${ENV_FILE}"
}

arch_map() {
  case "$(uname -m)" in
    x86_64) echo "amd64" ;;
    aarch64|arm64) echo "arm64" ;;
    armv7l|armv7*) echo "armv7" ;;
    armv6l|armv6*) echo "armv6" ;;
    i386|i686) echo "386" ;;
    riscv64) echo "riscv64" ;;
    mips64*) echo "mips64" ;;
    mipsel*) echo "mipsle" ;;
    mips*) echo "mips" ;;
    *) echo "unsupported" ;;
  esac
}

