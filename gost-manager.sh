main() {
  require_root
  prepare_env

  while true; do
    show_menu
    if ! prompt_read c "请选择 [0-19]: "; then
      echo -e "${YEL}检测到非交互输入，请使用: bash <(curl -fsSL URL) 或先下载后执行脚本。${RST}"
      exit 1
    fi
    case "${c}" in
      1) install_gost_latest ;;
      2) create_service; echo -e "${GRN}服务初始化完成${RST}" ;;
      3) list_rules ;;
      4) add_rule_quick ;;
      5) add_rule_custom ;;
      6) add_lb_template ;;
      7) delete_rule ;;
      8) create_service; reload_service; echo -e "${GRN}已应用规则${RST}" ;;
      9) systemctl start gost ;;
      10) systemctl stop gost ;;
      11) systemctl restart gost ;;
      12) service_status ;;
      13) install_acme ;;
      14) issue_cert_standalone ;;
      15) import_cert_manual ;;
      16) list_certs ;;
      17) enable_cron_restart ;;
      18) disable_cron_restart ;;
      19) uninstall_all ;;
      0) exit 0 ;;
      *) echo -e "${YEL}无效输入${RST}" ;;
    esac
  done
}

main "$@"
